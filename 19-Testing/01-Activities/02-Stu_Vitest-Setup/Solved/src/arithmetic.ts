// The class `Arithmetic` is defined and exported
export default class Arithmetic {

  // Method that returns the two provided numbers added together.
  sum(a: number, b: number) : number {
    return a + b;
  }

  // Method that returns the two provided numbers subtracted.
  difference(a: number, b: number) : number {
    return a - b;
  }

  // Method that returns the two provided numbers multiplied together.
  product(a: number, b: number) : number {
    return a * b;
  }

  // Method that returns the two provided numbers divided.
  quotient(a: number, b: number) : number {
    return a / b;
  }
};
